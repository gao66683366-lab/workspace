#!/usr/bin/env python3
import cairosvg
OUT = '/root/.openclaw/media/tool-image-generation/'

def sh(w,h): return f'<?xml version="1.0" encoding="UTF-8"?>\n<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {w} {h}">\n<rect width="{w}" height="{h}" fill="white"/>'
def R(x,y,w,h,fc='#D0E8F8',ec='#1A6FA8',lw=3,rx=10): return f'<rect x="{x}" y="{y}" width="{w}" height="{h}" rx="{rx}" fill="{fc}" stroke="{ec}" stroke-width="{lw}"/>'
def T(x,y,t,sz=22,wt='bold',fill='#1a1a1a',a='middle'):
    s=str(t).replace('&','&amp;').replace('<','&lt;').replace('>','&gt;')
    return f'<text x="{x}" y="{y}" font-family="Noto Sans CJK JP,sans-serif" font-size="{sz}" font-weight="{wt}" text-anchor="{a}" fill="{fill}">{s}</text>'
def S(x,y,t,sz=17,fill='#555',a='middle'):
    s=str(t).replace('&','&amp;').replace('<','&lt;').replace('>','&gt;')
    return f'<text x="{x}" y="{y}" font-family="Noto Sans CJK JP,sans-serif" font-size="{sz}" text-anchor="{a}" fill="{fill}">{s}</text>'
def L(x1,y1,x2,y2,sc='#888',lw=2): return f'<line x1="{x1}" y1="{y1}" x2="{x2}" y2="{y2}" stroke="{sc}" stroke-width="{lw}"/>'
def EN(): return '</svg>'
def sv(name,body,w,h):
    cairosvg.svg2png(bytestring=(sh(w,h)+body+EN()).encode(),write_to=f'{OUT}{name}.png',dpi=180)
    print(f'Saved {name}.png')

