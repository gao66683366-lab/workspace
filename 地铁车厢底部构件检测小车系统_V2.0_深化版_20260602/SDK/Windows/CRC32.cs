        public static uint Calc(byte[] data, int offset = 0, int length = -1)
        {
            if (length < 0) length = data.Length - offset;
            uint crc = 0xFFFFFFFF;
            for (int i = offset; i < offset + length; i++)
            {
                crc = Table[(crc ^ data[i]) & 0xFF] ^ (crc >> 8);
            }
            return crc ^ 0xFFFFFFFF;
